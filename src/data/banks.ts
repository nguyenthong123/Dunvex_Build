/**
 * Generate VietQR URL for bank transfer
 */
export function generateVietQRUrl(
  bankCode: string,
  accountNumber: string,
  accountName: string,
  amount?: number,
  description?: string
): string {
  const params = new URLSearchParams();
  params.set('accountName', accountName);
  if (amount && amount > 0) params.set('amount', amount.toString());
  if (description) params.set('addInfo', description.substring(0, 100));
  
  return `https://img.vietqr.io/image/${bankCode}-${accountNumber}-compact2.png?${params.toString()}`;
}

/**
 * Get bank short name by code
 */
export function getBankShortName(code: string): string {
  const bank = BANK_LIST.find(b => b.code === code);
  return bank?.shortName || code;
}
